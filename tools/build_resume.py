def load_data() -> dict:
    return json.loads(DATA_PATH.read_text(encoding="utf-8"))


def sanitize_text(value: str) -> str:
    normalized = unicodedata.normalize("NFKC", value)
    normalized = normalized.translate(TEXT_TRANSLATIONS)
    return normalized.replace("\t", " ")


def esc_text(value: str) -> str:
    return escape(sanitize_text(value), quote=True)


def esc_attr(value: str) -> str:
    return escape(value, quote=True)


def href(from_file: Path, target: Path, *, target_is_dir: bool = False) -> str:
    rel = os.path.relpath(target, start=from_file.parent).replace("\\", "/")
    if target_is_dir:
        if rel == ".":
            return "./"
        return rel.rstrip("/") + "/"
    return rel


def html_href(from_file: Path, target_file: Path) -> str:
    return os.path.relpath(target_file, start=from_file.parent).replace("\\", "/")


def icon_mime_type(path: Path) -> str:
    types = {
        ".svg": "image/svg+xml",
        ".ico": "image/x-icon",
        ".png": "image/png",
    }
    return types.get(path.suffix.lower(), "image/x-icon")



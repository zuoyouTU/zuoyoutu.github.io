def render_contact_link(url: str, text: str, *, kind: str | None = None) -> str:
    icon = render_contact_icon(kind) if kind else ""
    return '<span class="contact-item">{icon}<a href="{url}" target="_blank" rel="noopener noreferrer">{text}</a></span>'.format(
        icon=icon,
        url=esc_attr(url),
        text=esc_text(text),
    )


def render_main_contact_line(person: dict) -> str:
    icon_map = {"github": "github", "linkedin": "linkedin"}
    items = [render_contact_link(f'mailto:{person["email"]}', person["email"], kind="email")]
    for profile in person["profiles"]:
        items.append(render_contact_link(profile["url"], profile["label"], kind=icon_map.get(profile["label"].lower())))
    return join_contact_items(items)


def render_ats_contact_line(person: dict) -> str:
    icon_map = {"github": "github", "linkedin": "linkedin"}
    items = [render_contact_link(f'mailto:{person["email"]}', person["email"], kind="email")]
    for profile in person["profiles"]:
        items.append(render_contact_link(profile["url"], profile["display"], kind=icon_map.get(profile["label"].lower())))
    return join_contact_items(items)


def render_meta_line(person: dict) -> str:
    items: list[str] = []
    if person.get("relocation"):
        items.append(esc_text(person["relocation"]))
    if person.get("languages"):
        items.append(esc_text(person["languages"]))
    return join_contact_items(items)


def render_org(name: str, url: str | None, link_class: str | None) -> str:
    if not url:
        return esc_text(name)
    class_attr = f' class="{esc_attr(link_class)}"' if link_class else ""
    return '<a{class_attr} href="{url}" target="_blank" rel="noopener noreferrer">{name}</a>'.format(
        class_attr=class_attr,
        url=esc_attr(url),
        name=esc_text(name),
    )


def render_education_entries(entries: list[dict], link_class: str | None) -> str:
    blocks: list[str] = []
    for entry in entries:
        blocks.append(
            "\n".join(
                [
                    '      <div class="entry">',
                    '        <div class="entry-head">',
                    f'          <div class="entry-title">{render_org(entry["organization"], entry.get("url"), link_class)}</div>',
                    f'          <div class="entry-date">{esc_text(entry["dates"])}</div>',
                    "        </div>",
                    f'        <div class="entry-sub">{esc_text(entry["subtitle"])}</div>',
                    f'        <div class="entry-note">{esc_text(entry["note"])}</div>' if entry.get("note") else "",
                    "      </div>",
                ]
            ).replace("\n\n", "\n")
        )
    return "\n".join(blocks)


def render_experience_entries(entries: list[dict], link_class: str | None) -> str:
    blocks: list[str] = []
    for entry in entries:
        bullets = "\n".join(
            '          <li><span class="label">{label}:</span> {text}</li>'.format(
                label=esc_text(bullet["label"]),
                text=esc_text(bullet["text"]),
            )
            for bullet in entry["bullets"]
        )
        blocks.append(
            "\n".join(
                [
                    '      <div class="entry">',
                    '        <div class="entry-head">',
                    f'          <div class="entry-title">{render_org(entry["organization"], entry.get("url"), link_class)}</div>',
                    f'          <div class="entry-date">{esc_text(entry["dates"])}</div>',
                    "        </div>",
                    f'        <div class="entry-sub">{esc_text(entry["role"])}</div>',
                    "        <ul>",
                    bullets,
                    "        </ul>",
                    "      </div>",
                ]
            )
        )
    return "\n".join(blocks)


def render_skill_items(skills: list[dict]) -> str:
    return "\n".join(
        '        <p><span class="label">{label}:</span> {value}</p>'.format(
            label=esc_text(item["label"]),
            value=esc_text(item["value"]),
        )
        for item in skills
    )


def render_highlight_items(items: list[str]) -> str:
    return "\n".join(f"        <li>{esc_text(item)}</li>" for item in items)


def render_main_html(data: dict, outputs: dict[str, Path]) -> str:
    site = data["site"]
    person = data["person"]
    return GENERATED_COMMENT + MAIN_TEMPLATE.substitute(
        title=esc_text(site["main_title"]),
        favicon_href=esc_attr(href(outputs["main_html"], outputs["favicon"])),
        favicon_type=esc_attr(icon_mime_type(outputs["favicon"])),
        name=esc_text(person["name"]),
        pdf_wrapper_href=esc_attr(html_href(outputs["main_html"], outputs["main_wrapper"])),
        ats_html_href=esc_attr(html_href(outputs["main_html"], outputs["ats_html"])),
        contact_line_main=render_main_contact_line(person),
        meta_line=render_meta_line(person),
        summary=esc_text(data["summary"]),
        education_entries=render_education_entries(data["education"], "org-link"),
        skill_items=render_skill_items(data["skills"]),
        experience_entries=render_experience_entries(data["experience"], "org-link"),
    )


def render_ats_html(data: dict, outputs: dict[str, Path]) -> str:
    site = data["site"]
    person = data["person"]
    return GENERATED_COMMENT + ATS_TEMPLATE.substitute(
        title=esc_text(site["ats_title"]),
        favicon_href=esc_attr(href(outputs["ats_html"], outputs["favicon"])),
        favicon_type=esc_attr(icon_mime_type(outputs["favicon"])),
        ats_pdf_wrapper_href=esc_attr(html_href(outputs["ats_html"], outputs["ats_wrapper"])),
        name=esc_text(person["name"]),
        contact_line_ats=render_ats_contact_line(person),
        meta_line=render_meta_line(person),
        summary=esc_text(data["summary"]),
        education_entries=render_education_entries(data["education"], None),
        skill_items=render_skill_items(data["skills"]),
        experience_entries=render_experience_entries(data["experience"], None),
    )


def render_wrapper_html(data: dict, outputs: dict[str, Path], *, title: str, wrapper_key: str, pdf_key: str, frame_title: str) -> str:
    return GENERATED_COMMENT + WRAPPER_TEMPLATE.substitute(
        title=esc_text(title),
        favicon_href=esc_attr(href(outputs[wrapper_key], outputs["favicon"])),
        favicon_type=esc_attr(icon_mime_type(outputs["favicon"])),
        pdf_href=esc_attr(href(outputs[wrapper_key], outputs[pdf_key])),
        frame_title=esc_text(frame_title),
    )


def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.rstrip() + "\n", encoding="utf-8", newline="\n")


def find_browser() -> str | None:
    candidates = [
        shutil.which("msedge"),
        shutil.which("msedge.exe"),
        r"C:\Program Files (x86)\Microsoft\Edge\Application\msedge.exe",
        r"C:\Program Files\Microsoft\Edge\Application\msedge.exe",
        shutil.which("chrome"),
        shutil.which("chrome.exe"),
        r"C:\Program Files\Google\Chrome\Application\chrome.exe",
        r"C:\Program Files (x86)\Google\Chrome\Application\chrome.exe",
    ]
    for candidate in candidates:
        if candidate and Path(candidate).exists():
            return str(candidate)
    return None


def generate_pdf(browser: str, html_path: Path, pdf_path: Path) -> None:
    pdf_path.parent.mkdir(parents=True, exist_ok=True)
    command = [
        browser,
        "--headless",
        "--disable-gpu",
        "--allow-file-access-from-files",
        f"--print-to-pdf={pdf_path}",
        "--no-pdf-header-footer",
        html_path.resolve().as_uri(),
    ]
    result = subprocess.run(command, capture_output=True, text=True)
    if result.returncode != 0:
        message = result.stderr.strip() or result.stdout.strip() or "unknown error"
        raise RuntimeError(f"PDF generation failed for {html_path.name}: {message}")


def cleanup_obsolete_files() -> None:
    obsolete = [
        ROOT / "resume.pdf",
        ROOT / "resume.html",
        ROOT / "resume_ats.html",
        ROOT / "cequnyang_cpp.pdf",
        ROOT / "cequnyang_cpp_ats.pdf",
        ROOT / "cequnyang_cpp_ats.html",
    ]
    for path in obsolete:
        if path.exists():
            path.unlink()


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate resume site pages and PDFs from one data source.")
    parser.add_argument("--skip-pdf", action="store_true", help="Only regenerate HTML outputs.")
    args = parser.parse_args()

    data = load_data()
    site = data["site"]

    outputs = {
        "favicon": ROOT / "assets" / site["favicon"],
        "main_html": ROOT / "index.html",
        "main_wrapper": ROOT / "resume" / "index.html",
        "ats_html": ROOT / "ats" / "index.html",
        "ats_wrapper": ROOT / "ats" / "resume" / "index.html",
        "main_pdf": ROOT / "assets" / "pdf" / site["pdf_filename"],
        "ats_pdf": ROOT / "assets" / "pdf" / site["ats_pdf_filename"],
    }

    if not outputs["favicon"].exists():
        print(f"Missing favicon asset: {outputs['favicon']}", file=sys.stderr)
        return 1

    write_text(outputs["main_html"], render_main_html(data, outputs))
    write_text(outputs["ats_html"], render_ats_html(data, outputs))
    write_text(
        outputs["main_wrapper"],
        render_wrapper_html(
            data,
            outputs,
            title=site["main_title"],
            wrapper_key="main_wrapper",
            pdf_key="main_pdf",
            frame_title=f'{data["person"]["name"]} Resume',
        ),
    )
    write_text(
        outputs["ats_wrapper"],
        render_wrapper_html(
            data,
            outputs,
            title=site["ats_title"],
            wrapper_key="ats_wrapper",
            pdf_key="ats_pdf",
            frame_title=f'{data["person"]["name"]} ATS Resume',
        ),
    )

    cleanup_obsolete_files()

    if args.skip_pdf:
        print("Generated HTML outputs only.")
        return 0

    browser = find_browser()
    if not browser:
        print("Generated HTML outputs, but no supported browser was found for PDF export.", file=sys.stderr)
        return 1

    generate_pdf(browser, outputs["main_html"], outputs["main_pdf"])
    generate_pdf(browser, outputs["ats_html"], outputs["ats_pdf"])

    print(
        "Generated index.html, resume/index.html, ats/index.html, ats/resume/index.html, "
        "assets/pdf/{main_pdf}, and assets/pdf/{ats_pdf}.".format(
            main_pdf=site["pdf_filename"],
            ats_pdf=site["ats_pdf_filename"],
        )
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
